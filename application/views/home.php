	<ul class="column-center">
		I am VeeB and this is my personal website.
		<br /><br />
		What makes this website unique is that its design is <b>completely free of image data</b>.
		This includes all conceivable loopholes, such as <a target="_blank" href="http://en.wikipedia.org/wiki/Data_URI_scheme">data URIs</a> and <a target="_blank" href="http://fortawesome.github.io/Font-Awesome/">custom fonts</a>.
		All of the site's design is done via plain HTML and CSS.
		<br /><br />
		The premise behind this is that, for more complex images, if they can be done programmatically via pure HTML/CSS/Javascript, their programmed versions will almost always be significantly less in size than whatever image format they were in to begin with.
		This is because image formats inherently try to account for each and every single pixel inside the image, while HTML/CSS/Javascript can generalize collections of pixels into much more encompassing rulesets.
		<br /><br />
		Of course, <a target="_blank" href="/shenanigans/foodship9">sometimes</a>, <a target="_blank" href="/shenanigans/yuggoth_sample">images are unavoidable</a>, but even then one can easily minimize the space they take up and when they are loaded.
